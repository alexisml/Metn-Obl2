function Y=RK4(n,h,y0)
  T=zeros(n,4);
  Y=zeros(n,4);
  Y(1,1) = y0(1);
  Y(1,2) = y0(2);
  Y(1,3) = y0(3);
  Y(1,4) = y0(4);
  for k = 1:n-1
    [Tf1(1), Tf1(2), Tf1(3), Tf1(4)] = odeFunction(Y(k,1),Y(k,2),Y(k,3),Y(k,4));
    f1(1) = Tf1(1)
    f1(2) = Tf1(2)
    f1(3) = Tf1(3)
    f1(4) = Tf1(4)

    [Tf2(1), Tf2(2), Tf2(3), Tf2(4)] = odeFunction(Y(k,1)+(h/2*f1(1)),Y(k,2)+(h/2*f1(2)),Y(k,3)+(h/2*f1(3)),Y(k,4)+(h/2*f1(4)));
    f2(1) = Tf2(1)
    f2(2) = Tf2(2)
    f2(3) = Tf2(3)
    f2(4) = Tf2(4)

    [Tf3(1), Tf3(2), Tf3(3), Tf3(4)] = odeFunction(Y(k,1)+(h/2*f2(1)),Y(k,2)+(h/2*f2(2)),Y(k,3)+(h/2*f2(3)),Y(k,4)+(h/2*f2(4)));
    f3(1) = Tf3(1)
    f3(2) = Tf3(2)
    f3(3) = Tf3(3)
    f3(4) = Tf3(4)

    [Tf4(1), Tf4(2), Tf4(3), Tf4(4)] = odeFunction(Y(k,1)+(h/2*f3(1)),Y(k,2)+(h/2*f3(2)),Y(k,3)+(h/2*f3(3)),Y(k,4)+(h/2*f3(4)));
    f4(1) = Tf4(1)
    f4(2) = Tf4(2)
    f4(3) = Tf4(3)
    f4(4) = Tf4(4)

    Y(k+1,1) = Y(k,1) + (h/6)*(f1(1) + 2*(f2(1) + f3(1)) + f4(1));
    Y(k+1,2) = Y(k,2) + (h/6)*(f1(2) + 2*(f2(2) + f3(2)) + f4(2));
    Y(k+1,3) = Y(k,3) + (h/6)*(f1(3) + 2*(f2(3) + f3(3)) + f4(3));
    Y(k+1,4) = Y(k,4) + (h/6)*(f1(4) + 2*(f2(4) + f3(4)) + f4(4));
  endfor
endfunction